def Move(L,move):
    for i in range(len(L)):
      for j in range(len(L[i])):
        L[i][j] += move
    return L

def MakeMatriX(G,Max):
    l = [[0 for i in range(Max)] for j in range(8)]
    C = 1
    for i in G:
        for j in i:
            l[C][j] = '1'
        C += 1
    Printer(l)
    
def Printer(l):
    for i in l:
        for j in i:
            if j=='1': print("*",end="")
            else: print(end=" ")
        print()
    
def DotMatrix(Str):
    Def, Max = 0, 7*len(Str) + 2*len(Str) + 2
    G = [[],[],[],[],[],[],[]]
    for i in Str:
        L, C = Move(Library(i),Def), 0
        for i in L:
            G[C] = G[C]+i
            C += 1
        Def += 7
    MakeMatriX(G,Max)

